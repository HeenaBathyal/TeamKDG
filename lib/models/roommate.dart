class Roommate {
  final String name;
  final int age;
  final String city;
  final String gender;
  final double rent;
  final double compatibility;
  final String description;

  Roommate({
    required this.name,
    required this.age,
    required this.city,
    required this.gender,
    required this.rent,
    required this.compatibility,
    required this.description,
  });
}
